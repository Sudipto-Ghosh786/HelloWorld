package Day1;

public class Variable {
	public static void main(String[] args) {
		int intVal = 0;
		float floatVal = 370.0f;
		double doubleVal = 3900.50;
		boolean boolVal = true;
		System.out.println(
				"Integer\t:" + intVal 
				+ "\nFloat\t:" + floatVal 
				+ "\nDouble\t:" + doubleVal 
				+ "\nBoolean\t:" + boolVal
				);
	}

}
